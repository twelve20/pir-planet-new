// Modal elements
const modal = document.getElementById('productModal');
const modalBody = document.getElementById('modalBody');
const modalClose = document.getElementById('modalClose');
const modalOverlay = document.getElementById('modalOverlay');

// Open modal function
function openProductModal(productId) {
    const product = products[productId];
    if (!product) return;

    // Generate specs HTML
    const specsHTML = Object.entries(product.specs)
        .map(([key, value]) => `
            <div class="modal-spec-item">
                <strong>${key}</strong>
                <span>${value}</span>
            </div>
        `).join('');

    // Generate features HTML
    const featuresHTML = product.features
        .map(feature => `<li>${feature}</li>`)
        .join('');

    // Generate price HTML
    const priceHTML = product.priceCash ? `
        <span class="price-cash">${product.priceCash}</span>
        <span class="price-cashless">${product.priceCashless}</span>
        <span class="price-info">
            <svg viewBox="0 0 24 24" fill="none" stroke="currentColor">
                <circle cx="12" cy="12" r="10"></circle>
                <line x1="12" y1="16" x2="12" y2="12"></line>
                <line x1="12" y1="8" x2="12.01" y2="8"></line>
            </svg>
            <div class="price-tooltip">Скидка<br>Оплата наличными -20%</div>
        </span>
    ` : `${product.price}`;

    // Generate pack info HTML
    const packInfoHTML = product.packSize ? `
        <div class="modal-pack-info">
            <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" width="20" height="20">
                <path d="M21 16V8a2 2 0 00-1-1.73l-7-4a2 2 0 00-2 0l-7 4A2 2 0 003 8v8a2 2 0 001 1.73l7 4a2 2 0 002 0l7-4A2 2 0 0021 16z"></path>
            </svg>
            <span>В упаковке: ${product.packSize} шт</span>
        </div>
    ` : '';

    // Generate modal content
    modalBody.innerHTML = `
        <div class="modal-product-image">
            <img src="${product.image}" alt="${product.name}">
        </div>
        <div class="modal-product-info">
            <h2 class="modal-product-title">${product.name}</h2>
            <div class="modal-product-price">${priceHTML}</div>
            ${packInfoHTML}
            <p class="modal-product-description">${product.description}</p>

            <div class="modal-specs">
                <h4>Технические характеристики</h4>
                <div class="modal-specs-grid">
                    ${specsHTML}
                </div>
            </div>

            <div class="modal-features">
                <h4>Преимущества</h4>
                <ul>
                    ${featuresHTML}
                </ul>
            </div>

            <div class="modal-actions">
                <button class="btn-primary btn-full" onclick="addToCartFromModal(${product.id})">В корзину</button>
                <a href="#order" class="btn-secondary btn-full" onclick="closeProductModal()">Заказать</a>
            </div>
        </div>
    `;

    // Show modal
    modal.classList.add('active');
    document.body.style.overflow = 'hidden';
}

// Close modal function
function closeProductModal() {
    modal.classList.remove('active');
    document.body.style.overflow = '';
}

// Add to cart from modal
function addToCartFromModal(productId) {
    const product = products[productId];

    if (!product) {
        console.error('Товар не найден');
        return;
    }

    // Извлекаем числовую цену из строки (например, "622 ₽" -> 622)
    const priceMatch = product.price.match(/[\d\s]+/);
    const price = priceMatch ? parseFloat(priceMatch[0].replace(/\s/g, '')) : 0;

    const cartProduct = {
        sku: product.sku || `product-${productId}`,
        name: product.name,
        price: price,
        quantity: product.packSize || 1,
        packSize: product.packSize || null,
        image: product.image
    };

    // Используем глобальную функцию cart из cart.js
    if (window.cart) {
        window.cart.addItem(cartProduct);

        // Находим кнопку в модальном окне и показываем визуальный эффект
        const addButton = document.querySelector('.modal-actions .btn-primary');
        if (addButton) {
            addButton.classList.add('added');
            const originalText = addButton.textContent;
            addButton.textContent = '✓ Добавлено';

            setTimeout(() => {
                addButton.classList.remove('added');
                addButton.textContent = originalText;
            }, 2000);
        }
    } else {
        console.error('Корзина не инициализирована');
    }
}

// Event listeners
document.addEventListener('DOMContentLoaded', () => {
    // Add click handlers to all product detail buttons
    const detailButtons = document.querySelectorAll('.product-details-btn');
    detailButtons.forEach(button => {
        button.addEventListener('click', (e) => {
            e.preventDefault();
            const productId = parseInt(button.getAttribute('data-product'));
            openProductModal(productId);
        });
    });

    // Close modal on close button click
    modalClose.addEventListener('click', closeProductModal);

    // Close modal on overlay click
    modalOverlay.addEventListener('click', closeProductModal);

    // Close modal on ESC key
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && modal.classList.contains('active')) {
            closeProductModal();
        }
    });

    // Make price info buttons work on mobile (click to toggle)
    document.addEventListener('click', (e) => {
        const priceInfo = e.target.closest('.price-info');

        if (priceInfo) {
            e.preventDefault();
            e.stopPropagation();

            // Close all other active price-info tooltips
            document.querySelectorAll('.price-info.active').forEach(el => {
                if (el !== priceInfo) {
                    el.classList.remove('active');
                }
            });

            // Toggle current tooltip
            priceInfo.classList.toggle('active');
        } else {
            // Close all tooltips when clicking outside
            document.querySelectorAll('.price-info.active').forEach(el => {
                el.classList.remove('active');
            });
        }
    });

    // Note: Cart button handlers are in cart.js via onclick attributes
    // We don't add event listeners here to avoid duplicate calls
});
